package edu.handong.java.csee.lab09;

public class Salesman {
	String name;
	double sales;
	
	public void setName(String name){
		this.name = name;
	}
	
	public void setSales(double sales) {
		this.sales = sales;
	}
	
	public String getName() {
		return this.name;
	}
	public double getSales() {
		return this.sales;
	}
	
}
